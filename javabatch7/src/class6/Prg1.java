package class6;

public class Prg1 {
    public static void main(String[] args) {
        // increment and decrement(increase value by 1,++,--,i+=5)
        int a = 1;
        System.out.println("hello");
        System.out.println(a);
        System.out.println("i want to increase the value of a");
        a = 2;
        System.out.println(a);
        a = a + 1;
        System.out.println(a);
        a++;
        System.out.println(a);
    }
}
